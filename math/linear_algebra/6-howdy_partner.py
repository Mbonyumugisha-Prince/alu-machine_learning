#!/usr/bin/env python3
"""
This module contains a function to concatenate two arrays.
"""


def cat_arrays(arr1, arr2):
    """
    Concatenate two arrays into a new list.

    Args:
        arr1 (list of int/float): First array.
        arr2 (list of int/float): Second array.

    Returns:
        list: New array containing elements of arr1 followed by arr2.
    """
    return arr1 + arr2
